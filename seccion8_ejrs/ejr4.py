numero1 = int(input("ingresa el primer numero: "))
numero2 = int(input("ingresa el segundo numero: "))

print(f"{numero1} + {numero2} = {numero1+numero2}")
print(f"{numero1} - {numero2} = {numero1-numero2}")
print(f"{numero1} * {numero2} = {numero1*numero2}")
print(f"{numero1} / {numero2} = {numero1/numero2}")